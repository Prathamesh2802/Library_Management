    private void add_booksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_booksActionPerformed
        // TODO add your handling code here:
        try{ 
         FileInputStream fis = null;  
         int Bookid = Integer.parseInt(bookid.getText());
         String Book_name = book_name.getText();
         String Publications = publication.getText();
         try {
            year.commitEdit();
            stock.commitEdit();
            department.commitEdit();;
         } 
         catch ( java.text.ParseException e ) { 
         }
            String Department = (String) department.getValue();
            int Year = (Integer) year.getValue();
            int Stock = (Integer) stock.getValue();
            String Author = author_name.getText();
           DB_Connection con = new DB_Connection();
            File fileselected = jf.getSelectedFile();
            String filepath = fileselected.getAbsolutePath();
            fis = new FileInputStream(filepath);  
            String sql = "insert into books(bookid, bookcover, book_name, publications, department, year, stock, Author) values(?,?,?,?,?,?,?,?)";
            PreparedStatement ps = con.c.prepareStatement(sql);
            //st.setBinaryStream(4, (InputStream) fis, imageLength);  
            ps.setInt(1, Bookid);
            ps.setBinaryStream(2,fis);
            ps.setString(3, Book_name);
            ps.setString(4, Publications);
            ps.setString(5, Department);
            ps.setInt(6, Year);
            ps.setInt(7, Stock);
            ps.setString(8, Author);
            int count = ps.executeUpdate();  
            if (count > 0) {  
                JOptionPane.showMessageDialog(this, "Data Added Successfully");  
            } else {  
                JOptionPane.showMessageDialog(this, "Error Saving Data");  
            } 
        }
        catch(HeadlessException | FileNotFoundException | NumberFormatException | SQLException e)
        {
            e.printStackTrace();
        }
      
         
        
    }//GEN-LAST:event_add_booksActionPerformed

