    private void upimgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upimgActionPerformed
        // TODO add your handling code here:
        jf = new JFileChooser();
        jf.setCurrentDirectory(new File(System.getProperty("user.dir")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "png", "jpg");
        jf.addChoosableFileFilter(filter);            
        jf.setFileFilter(filter);
        int response = jf.showSaveDialog(null);
        if(response == JFileChooser.APPROVE_OPTION)
        {
//            File file = new File(jf.getSelectedFile().getName());
            String fileName = jf.getSelectedFile().getName();  
            String extension = fileName.substring(fileName.lastIndexOf("."));            
             if (extension.equalsIgnoreCase(".jpg") || extension.equalsIgnoreCase(".png")  ) {  
           imgpath.setText(jf.getSelectedFile().getPath());
             }
             else {  
                    JOptionPane.showMessageDialog(this, "Kindly Select Image File Only", "Error", JOptionPane.ERROR_MESSAGE);  
                }  
        }
    }//GEN-LAST:event_upimgActionPerformed

